package edu.usf.mail.rothm.vidyagame.entities;

public class LivingThing {
	private int health;
	private String name;

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setHealth(int health) {
		this.health = health;
	}

	public int getHealth() {
		return health;
	}
}
